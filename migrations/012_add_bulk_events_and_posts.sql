-- Generate likes for posts (average 5-15 likes per post from different users)
INSERT INTO likes (user_id, likeable_type, likeable_id, created_at)
SELECT 
    users.id as user_id,
    'post' as likeable_type,
    posts.id as likeable_id,
    posts.created_at + ((random() * INTERVAL '24 hours')) as created_at
FROM posts
CROSS JOIN (SELECT id FROM users LIMIT 6) as users
WHERE random() < 0.6  -- 60% chance each user likes each post
ON CONFLICT DO NOTHING;

