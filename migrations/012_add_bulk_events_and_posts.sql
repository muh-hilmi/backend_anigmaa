-- ============================================================================
-- EVENT ATTENDEES (Random attendees per event)
-- ============================================================================

-- Generate event attendees (5-25 attendees per event)
INSERT INTO event_attendees (event_id, user_id, status, joined_at)
SELECT 
    events.id as event_id,
    users.id as user_id,
    CASE 
        WHEN random() < 0.8 THEN 'confirmed'
        WHEN random() < 0.9 THEN 'pending'
        ELSE 'cancelled'
    END as status,
    events.created_at + ((random() * INTERVAL '14 days')) as joined_at
FROM events
CROSS JOIN (SELECT id FROM users LIMIT 6) as users
WHERE random() < 0.7  -- 70% chance each user joins each event
ON CONFLICT DO NOTHING;

-- ============================================================================
-- UPDATE COUNTERS
-- ============================================================================

-- Update likes count for all posts
UPDATE posts SET likes_count = (
    SELECT COUNT(*) FROM likes
    WHERE likeable_type = 'post' AND likeable_id = posts.id
);

-- Update comments count for all posts  
UPDATE posts SET comments_count = (
    SELECT COUNT(*) FROM comments
    WHERE post_id = posts.id
);

-- Update attendees count for all events
UPDATE events SET attendees_count = (
    SELECT COUNT(*) FROM event_attendees
    WHERE event_id = events.id AND status = 'confirmed'
);

